package algoritmim2;

public class Edge {
	
	int vertex1; // מאיזה קודקוד יוצא
	int vertex2; // לאיזה קודקוד מגיע
	double price; // עלות הצלע
	
	
	public Edge(int vertex1, int vertex2, double price) {
		super();
		this.vertex1 = vertex1;
		this.vertex2 = vertex2;
		this.price = price;
	}
	
	

}
